    private void readFromFileAndDisplayPhanSo() {
        try {
            ObjectInputStream ois = new ObjectInputStream(new BufferedInputStream(new FileInputStream("phanso.dat")));
            Object obj;
            txt_ketqua.setText("");

            while (true) {
                try {
                    obj = ois.readObject();
                    PhanSo ps = (PhanSo) obj;
                    txt_ketqua.append(ps.toString() + "\n");
                } catch (EOFException e) {
                    // End of file reached
                    break;
                }
            }
            ois.close();
            JOptionPane.showMessageDialog(this, "Đã đọc phân số từ file và hiển thị lên TextArea.");
        }catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

    
