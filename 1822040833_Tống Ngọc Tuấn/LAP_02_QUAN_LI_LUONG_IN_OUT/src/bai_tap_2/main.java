    private void createAndWritePhanSoToFile() {
        try {
            int numberOfPhanSo = Integer.parseInt(txtNhap.getText());

            FileOutputStream fos = new FileOutputStream("phanso.dat");
            BufferedOutputStream bos = new BufferedOutputStream(fos);
            ObjectOutputStream oos = new ObjectOutputStream(bos);

            for (int i = 0; i < numberOfPhanSo; i++) {
                PhanSo ps = new PhanSo(true);
                oos.writeObject(ps);
            }

            oos.close();
            JOptionPane.showMessageDialog(this, "Đã tạo và ghi phân số vào file.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi: " + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }
